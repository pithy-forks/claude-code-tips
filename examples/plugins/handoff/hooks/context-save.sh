#!/usr/bin/env bash
# context-save.sh — PreCompact hook
# Reads the PreCompact payload from stdin, uses transcript_path to extract
# recent context, and writes a structured handoff file to .claude/handoff.md

set -euo pipefail
# tested with: claude code v2.1.118

HANDOFF_DIR=".claude"
HANDOFF_FILE="${HANDOFF_DIR}/handoff.md"
TIMESTAMP=$(date -u +"%Y-%m-%dT%H:%M:%SZ")

# Read JSON payload from stdin
INPUT=$(cat)

# Ensure .claude directory exists
mkdir -p "${HANDOFF_DIR}"

# Extract session ID and transcript path from the PreCompact payload
SESSION_ID=$(printf '%s' "${INPUT}" | jq -r '.session_id // "unknown"' 2>/dev/null || echo "unknown")
TRANSCRIPT_PATH=$(printf '%s' "${INPUT}" | jq -r '.transcript_path // ""' 2>/dev/null || echo "")

# Try to extract recent context from the transcript file
LAST_MESSAGES=""
if [ -n "$TRANSCRIPT_PATH" ] && [ -f "$TRANSCRIPT_PATH" ]; then
  # Read the last 50 lines of the transcript for recent context
  LAST_MESSAGES=$(tail -50 "$TRANSCRIPT_PATH" 2>/dev/null || echo "Could not read transcript.")
fi

# Write the handoff file
cat > "${HANDOFF_FILE}" << EOF
# Session Handoff
**Saved:** ${TIMESTAMP}
**Trigger:** PreCompact (context window about to be compressed)
**Session:** ${SESSION_ID}

## What to do with this file

This was auto-generated when the context window filled up. Read it at the
start of your next session to pick up where you left off. Add your own notes
below before starting a new session.

## Recent Context

${LAST_MESSAGES:-"No transcript available. Add manual notes here about what you were working on."}

## Your Notes

<!-- Add your plan, progress, and blockers here before starting a new session -->

---
*Auto-generated by context-handoff v0.2.0*
EOF

echo "context-handoff: saved to ${HANDOFF_FILE}" >&2
