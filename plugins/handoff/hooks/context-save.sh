#!/usr/bin/env bash
# context-save.sh — PreCompact hook
# Reads the transcript from stdin (JSON), extracts key context,
# and writes a structured handoff file to .claude/handoff.md

set -euo pipefail

HANDOFF_DIR=".claude"
HANDOFF_FILE="${HANDOFF_DIR}/handoff.md"
TIMESTAMP=$(date -u +"%Y-%m-%dT%H:%M:%SZ")

# Read JSON payload from stdin
INPUT=$(cat)

# Ensure .claude directory exists
mkdir -p "${HANDOFF_DIR}"

# Extract the last N assistant messages from the transcript
# The PreCompact payload includes the conversation transcript
LAST_MESSAGES=$(echo "${INPUT}" | jq -r '
  .transcript // .messages // []
  | map(select(.role == "assistant"))
  | .[-5:]
  | .[].content // ""
' 2>/dev/null || echo "")

# Extract any tool usage context
TOOL_CONTEXT=$(echo "${INPUT}" | jq -r '
  .transcript // .messages // []
  | map(select(.role == "assistant" and .tool_use != null))
  | .[-3:]
  | .[].tool_use.name // ""
' 2>/dev/null || echo "")

# Extract session ID if available
SESSION_ID=$(echo "${INPUT}" | jq -r '.session_id // "unknown"' 2>/dev/null || echo "unknown")

# Write the handoff file
cat > "${HANDOFF_FILE}" << EOF
# Session Handoff
**Saved:** ${TIMESTAMP}
**Trigger:** PreCompact
**Session:** ${SESSION_ID}

## Last Context

The following was extracted from the final assistant messages before context compression:

\`\`\`
${LAST_MESSAGES}
\`\`\`

## Recent Tool Activity

${TOOL_CONTEXT:-"No tool usage captured."}

## Recovery Instructions

This file was auto-generated by the context-handoff plugin when Claude Code's
context window was about to be compressed. To resume work:

1. Read this file at the start of your next session
2. Use it to reconstruct your plan and progress
3. The file will be overwritten on the next PreCompact event

---
*Auto-generated by context-handoff v0.1.0*
EOF

echo "context-handoff: saved to ${HANDOFF_FILE}" >&2
